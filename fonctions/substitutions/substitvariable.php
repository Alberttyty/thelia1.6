/* Substitutions de type variable */
function substitvariable($texte){

	$matches = array();

	if (preg_match_all("`\#VARIABLE\(([^\)]+)\)`", $texte, $matches, PREG_SET_ORDER)) {
		foreach($matches as $match) {
			$texte = str_replace($match[0], Variable::lire($match[1]), $texte);
		}
	}

    return $texte;
}
