	function trad($mot, $nom = ""){
		$dico = "dico_" . $nom;

		if (isset($GLOBALS[$dico][$mot]))
			$message = $GLOBALS[$dico][$mot];
		else
			$message = $mot;

		$args = func_get_args();

		array_shift($args);
		array_shift($args);

		return vsprintf($message, $args);
	}
